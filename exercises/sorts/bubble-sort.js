//BUBBLE SORT
//REMEBER, BUBBLE SORT MAKES SURE BIGGEST NUMBER ENDS UP LAST EACH ITERATION
// O(n^2) - NOT THAT GOOD
const numbers2 = [99, 44, 6, 2, 1, 5, 63, 87, 283, 4, 0];

function bubbleSort(array) {
  const length = array.length;
  for (let i = 0; i < length; i++) {
    for (let j = 0; j < length; j++) { 
      if(array[j] > array[j+1]) {
        //Swap the numbers
        let temp = array[j]
        array[j] = array[j+1];
        array[j+1] = temp;
      }
    }        
  }
}

bubbleSort(numbers2);
